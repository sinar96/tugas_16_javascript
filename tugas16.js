function MinMax() {
    var a = Math.min(45, 100, 99, 3, 2, 4, 1, 2, 3, 19, 22, 14, 10);
    var b = Math.max(45, 100, 99, 3, 2, 4, 1, 2, 3, 19, 22, 14, 10);
    console.log("Nilai Minimal : ", a);
    console.log("Nilai Maximal : ", b);
}
MinMax();

